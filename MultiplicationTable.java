public class MultiplicationTable {

    public static void main(String[] args) {
        // Define the number for which the multiplication table is to be printed
        int number = 5;  // You can change this value to print the table for a different number
        
        // Loop to print the multiplication table of the number
        for (int i = 1; i <= 10; i++) {
            System.out.println(number + " x " + i + " = " + (number * i));
        }
    }
}
 
    

